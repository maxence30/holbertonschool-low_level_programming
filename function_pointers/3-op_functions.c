#include "3-calc.h"

/**
 * op_add - additionne deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: résultat de a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - soustrait deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: résultat de a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplie deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: résultat de a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divise deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: résultat de a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calcule le modulo de deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: résultat de a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
